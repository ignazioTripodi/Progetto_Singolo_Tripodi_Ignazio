/**
  * @brief This function handles TIM2 global interrupt.
  */
void TIM2_IRQHandler(void) {
	/* USER CODE BEGIN TIM2_IRQn 0 */

	HAL_ADC_Start(&hadc1); //avvia la conversione ADC utilizzando la periferica ADC1
	HAL_ADC_PollForConversion(&hadc1, 1000);//attende la fine della conversione fino ad un massimo di 1000 ms
	value = HAL_ADC_GetValue(&hadc1);//ottengo il valore di tensione convertito dalla periferica ADC e lo assegno a value
	HAL_ADC_Stop(&hadc1);//viene fermata la conversione
	char bufferTx[50];//creazione di un array di caratteri di lunghezza 50. Viene utilizzato per memorizzare dati al suo interno
	value = value * 3300 / 255;//calcola il valore di tensione corrispondente al valore convertito. Moltiplica il valore per 3300 (riferito alla Vdd in mV) e divide per 255 (massimo valore di conversione ADC a 8 bit)
	temp=value*30/3300;//calcola il valore di temperatura corrispondente al valore di tensione. Applica una relazione lineare per convertire la tensione in temperatura
	sprintf(bufferTx, "%Lu mV %Lu C°\r\n", value, temp); //viene utilizzata per formattare una stringa all'interno del buffer e per specificare dove inserire i valori. bufferTx indica il buffer di caratteri in cui verrà memorizzata la stringa formattata. %Lu indica unsigned long integer, indicando che la variabile da formattare è di tipo intero, senza segno. value e temp sono i valori da inserire nella stringa.
	HAL_UART_Transmit(&huart2, (uint8_t*) bufferTx, strlen(bufferTx), 1000);//trasmette il contenuto del bufferTx tramite la periferica UART. strlen(bufferTx)indica la lunghezza della stringa presente nel buffer bufferTx, mentre 1000 (ms) indica il tempo massimo di attesa per completare l'operazione di trasmissione

	__HAL_TIM_SET_COUNTER(&htim2, 0);//setta il timer a 0
	HAL_TIM_Base_Start_IT(&htim2);//avvia il TIM2 in modalità interrupt

	/* USER CODE END TIM2_IRQn 0 */
	HAL_TIM_IRQHandler(&htim2);
	/* USER CODE BEGIN TIM2_IRQn 1 */

	/* USER CODE END TIM2_IRQn 1 */
}

